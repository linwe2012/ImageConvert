Image colorSpaceTransform(Image dst, Image src, double mat[3][3], double vec[3])
{
 int i, j, temp;
 image_t R, G, B,max=0,min=9999;
 int row = src->bmpInfo->biHeight;
 int col = src->bmpInfo->biWidth;
 int row_width = (3 * col + 3) & ~3;
 Pixel *sp, *dp, *pp;
 int cnt = 0;
 if (dst == ((void *)0)) {
  dst = newImage();
 }
 if (src != dst) resizeImage(dst, row_width * row);
 for (i = 0; i<row; i++) {
  temp = row_width * i;
  sp = (Pixel *)(src->im + temp);
  dp = (Pixel *)(dst->im + temp);
  for (j = 0; j<col; j++, sp++, dp++) {
   R = sp->R;
   G = sp->G;
   B = sp->B;

   dp->Y = mat[0][0] * R + mat[0][1] * G + mat[0][2] * B + vec[0];
   dp->U = mat[1][0] * R + mat[1][1] * G + mat[1][2] * B + vec[1];
   dp->V = mat[2][0] * R + mat[2][1] * G + mat[2][2] * B + vec[2];
   pp = dp;
   cnt ++;
   if(pp->B > max) max = pp->B;
   if(pp->B < min) min = pp->B;

  }
 }
 printf("colorSpaceTransform::max=%lf,min=%lf, cnt=%d, row=%d, col=%d\n",max,min,cnt,row, col);
 copyImageInfo(dst, src);
 return dst;
}


